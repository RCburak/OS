void draw_char(char c, int x, int y, unsigned char color) {
    if (c < 0 || c > 127) return;
    unsigned char* glyph = font8x8[(int)c];
    for (int i = 0; i < 8; i++) {
        for (int j = 0; j < 8; j++) {
            if ((glyph[i] >> j) & 1) put_pixel(x + i, y + j, color);
        }
    }
}

void draw_string(char* s, int x, int y, unsigned char color) {
    while (*s) {
        draw_char(*s++, x, y, color);
        x += 8;
    }
}

void draw_window(Window* win) {
    draw_rect(win->x, win->y, win->w, win->h, COL_WINDOW_BG);
    draw_rect(win->x, win->y, win->w, 18, COL_TITLE_BAR);
    draw_string(win->title, win->x + 5, win->y + 5, COL_TITLE_TEXT);
    draw_rect(win->x + win->w - 14, win->y + 4, 10, 10, COL_BUTTON_CLOSE);
}

void draw_icon(Icon* icon) {
    draw_rect(icon->x, icon->y, 32, 32, icon->color);
    draw_string(icon->label, icon->x, icon->y + 36, 15);
}